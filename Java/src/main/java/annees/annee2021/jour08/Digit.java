package annees.annee2021.jour08;

public class Digit {

	private int nombre;
	private String wires;

	public Digit(String wires, int nombre) {
		super();
		this.nombre = nombre;
		this.wires = wires;
	}

	public int getNombre() {
		return nombre;
	}

	public void setNombre(int nombre) {
		this.nombre = nombre;
	}

	public String getWires() {
		return wires;
	}

	public void setWires(String wires) {
		this.wires = wires;
	}

}

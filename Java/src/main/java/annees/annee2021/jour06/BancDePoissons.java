package annees.annee2021.jour06;

public class BancDePoissons {

	private int numero;
	private long nombre;

	public BancDePoissons(int numero, long nombre) {
		super();
		this.numero = numero;
		this.nombre = nombre;
	}

	public int getNumero() {
		return numero;
	}

	public void setNumero(int numero) {
		this.numero = numero;
	}

	public long getNombre() {
		return nombre;
	}

	public void setNombre(long nombre) {
		this.nombre = nombre;
	}

}
